package com.example.grofer.FruitsAndVegetables;

public class FruitsVegetableOne {

    private String ivUrl;
    private String tvFruitsDetailsOne;

    public FruitsVegetableOne(String ivUrl, String tvFruitsDetailsOne) {
        this.ivUrl = ivUrl;
        this.tvFruitsDetailsOne = tvFruitsDetailsOne;
    }

    public String getIvUrl() {
        return ivUrl;
    }

    public String getTvFruitsDetailsOne() {
        return tvFruitsDetailsOne;
    }
}
